# Python Operators
print(10 + 5)
'''
Python divides the operators in the following groups:

    1.Arithmetic operators
    2.Assignment operators
    3.Comparison operators
    4.Logical operators
    5.Identity operators
    6.Membership operators
    7.Bitwise operators

'''

# Arithmetic operators (+,-,*,/,%,//)

x=20
y=30
print(x+y)
print(x-y)
print(x/y)
print(x%y)
print(x//y)
print(x*y)

# Assignment operators (=,+=,-=,*=,/=,%=.......)
taka=50
pen=10
pen+=10
print(pen)

print(f"I can buy this pen {int(taka/pen)} pieces") if taka>pen else print("I can't buy this pen")
